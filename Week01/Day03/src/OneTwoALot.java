public class OneTwoALot {
    public static void main(String[] args) {
        int a;
        a = 8;
        if (a <= 0) {
            System.out.println ("Not enough");
        }
        if (a == 1) {
            System.out.println("One");
        }
        if (a==2) {
            System.out.println("Two");
        }
        if(a>2) {
            System.out.print("A lot");
        }

    }
}